public class UsingMod {

    /**
     * Show the difference between the % and /.
     * @param args
     */
    public static void main(String[] args) {
        int x = 20;
        int y = 7;
        int div = x/y;
        int mod = x%y;
        System.out.println(div);
        System.out.println(mod);
    }
}